<Poem
	title="The Other Side"
	date="September 8, 2017"
	imgSource="/the-waiting/the-other-side-banner.webp"
>
	<p class="poem-verse">
		Here I am on the other side,<br />

		Looking ‘cross this vast chasm in your direction,<br />

		My view clouded by darkness and dust.
	</p>

	<p class="poem-verse">
		I can’t see your face.<br />

		I can’t see whether you’re looking back at me<br />

		Or looking away, your back to my face.
	</p>

	<p class="poem-verse">
		And when you look this way,<br />

		I can’t tell whether you are pleased or disgusted,<br />

		Or whether you are indifferent.
	</p>

	<p class="poem-verse">
		Here I am standing alone<br />

		On the other side of this vast chasm<br />

		While you, for all I can see, are surrounded by a sea of joys<br />

		And maybe even bountiful reservoirs of slender fish.
	</p>

	<p class="poem-verse">
		Here I am alone and sinking in my own sea<br />

		While you—and this I was able to glimpse—<br />

		Are blazing heartily through and above the waters,<br />

		Without a thought of worry or doubt.
	</p>

	<p class="poem-verse">
		Here I am, watching your tracks<br />

		Which seem to push me away,<br />

		And I wonder whether you’re <em>trying</em> to<br />

		Or are merely compelled to.
	</p>

	<p class="poem-verse">
		And I wonder whether your side still has a heart for me<br />

		Or whether I am a pest in your eyes.
	</p>

	<p class="poem-verse">
		Yet here I am, still waiting.<br />

		Waiting for the day you might turn around and look at me<br />

		Waiting for the day when you might break through the gorge<br />

		And touch me.
	</p>

	<p class="poem-verse">Waiting for that promise that it seems may never come.</p>
</Poem>
