<Poem title="Touch Grass" date="February 9, 2024" imgSource="/the-waiting/touch-grass-banner.webp">
	<p class="poem-verse">
		I wish that I could touch grass,<br />

		But every time my foot grazes the surface,<br />

		My soul burns against the sunbaked earth,<br />

		And the fresh blades scald my flesh.
	</p>

	<p class="poem-verse">
		And so I leap up into cyberspace<br />

		And retreat behind the virtual walls of my empty abode,<br />

		Praying for another scar to heal,<br />

		Before I try again.
	</p>
</Poem>
