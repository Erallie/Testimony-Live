<Poem title="An Outcast" date="November 14, 2017" imgSource="/the-waiting/an-outcast-banner.webp">
	<p class="poem-prose">
		I look around me. Towering above are my “friends” of old, now content among themselves without
		me. I remember when we used to be equals, when I used to compete with them for the highest and
		used to shine above them.
	</p>

	<p class="poem-prose">
		Now I am with a herd not my own, whose statures were below mine and whose manes did not shine as
		bright as mine. But now I am just as tall as they. They have grown while I have not. My mane is
		dampened with clay so as not to shine at all. I am now one of them, though their hooves will
		never thud to the same rhythm as mine, and my colors will never truly match theirs.
	</p>

	<p class="poem-prose">
		I belong to the older herd, whose time on this earth have made them wiser and higher and made
		their manes shine brighter. I am one of them, yet I am not as tall as they. I have lost the
		spark that made me glow and lost the time that would have made me grow. I am not one of them. I
		am now an outcast.
	</p>
</Poem>
