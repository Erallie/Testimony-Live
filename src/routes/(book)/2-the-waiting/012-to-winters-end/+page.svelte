<Poem
	title="To Winter's End"
	date="September 19, 2023"
	imgSource="/the-waiting/to-winters-end-banner.webp"
>
	<p class="poem-verse">I may be waiting for grass to grow.</p>

	<p class="poem-verse">
		I may be waiting for ice to thaw<br />

		And snow to melt.
	</p>

	<p class="poem-verse">
		I may be counting the icicles as they fall from the frozen cliffside,<br />

		Praying for spring to dawn upon this barren earth<br />

		So that seeds may take root in the hills<br />

		And flowers may bloom once again.
	</p>

	<p class="poem-verse">
		And as I wait for the grass to grow,<br />

		I may wonder how to make the frost less biting<br />

		Or the ice less sharp.
	</p>

	<p class="poem-verse">
		I may wonder if the wind will keep on stinging<br />

		Or if I may find solace in a fire pit<br />

		Until the sun melts the ice away<br />

		And spring reemerges from beneath the frost.
	</p>

	<p class="poem-verse">
		And although I have no wood,<br />

		And there is nothing but ice and snow on the horizon,
	</p>

	<p class="poem-verse">
		Every day, darkness devours the sky,<br />

		And every day, the sun breaks through to touch the earth’s soil.<br />

		Every year, the deserts grow dry,<br />

		And every year, the rain comes to quench its thirst.
	</p>

	<p class="poem-verse">
		And although the cold of winter may have turned my memory brittle,<br />

		I still remember the hummingbird’s shining colors<br />

		And the lilac’s sweet scent;<br />

		I still remember the mourning dove’s tender call<br />

		And the morning’s radiant glow;
	</p>

	<p class="poem-verse">
		And I must believe<br />

		That as constant as the ticking of the clock<br />

		And as surely as the dusk turns to dawn,<br />

		The sun will wake the frozen earth<br />

		And life will blossom once more.
	</p>

	<p class="poem-verse">
		And so long as there is still the faintest light in the night sky,<br />

		I will march on<br />

		Until I am there to see it.
	</p>
</Poem>
