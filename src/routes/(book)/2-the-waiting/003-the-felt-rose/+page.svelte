<Poem
	title="The Felt Rose"
	date="September 9, 2017"
	imgSource="/the-waiting/the-felt-rose-banner.webp"
>
	<p class="poem-verse">
		A little more than a trifle<br />

		A sign of love and amor<br />

		A small token of thought
	</p>

	<p class="poem-verse">
		A fabricated show of life<br />

		A plastic toy<br />

		A sign of fake reality
	</p>

	<p class="poem-verse">
		A remembrance of our parting<br />

		A memory of what is gone<br />

		A symbol of what is lost
	</p>

	<p class="poem-verse">
		A memory of your infidelity<br />

		Of the day you turned away<br />

		And left me alone to weep
	</p>

	<p class="poem-verse">
		A token of that day<br />

		When you walked away<br />

		And closed the door in my face
	</p>

	<p class="poem-verse">
		I take it<br />

		But not from you<br />

		And keep it next to your memory
	</p>

	<p class="poem-verse">
		To remember always<br />

		What I may never have again
	</p>
</Poem>
