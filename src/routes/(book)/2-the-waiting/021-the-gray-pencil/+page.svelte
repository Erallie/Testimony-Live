<Poem
	title="The Gray Pencil"
	date="June 28, 2024"
	imgSource="/the-waiting/the-gray-pencil-banner.webp"
>
	<p class="poem-prose">
		Her quivering fingers extend themselves limply toward the gray pencil. Flashes of quaking
		memories flood her brain. She inhales and shuts her eyes. <em>She can do this.</em> The soul
		weighing down her stomach and pinning it to inaction cries out, “I do not <em>want</em> to. I do
		not
		<em>care.”</em> She exhales slowly. <em>She does care. She only needs to try.</em> Her eyes open
		with aching resolve. <em>One more inch…</em> Her fingers feebly caress the gray pencil as the
		touch of the cold wood scathes her skin. “I hate you,” her stomach mumbles. She takes another
		painful breath. <em>Now to write…</em>
	</p>
</Poem>
