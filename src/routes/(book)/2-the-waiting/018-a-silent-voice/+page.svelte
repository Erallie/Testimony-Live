<Poem
	title="A Silent Voice"
	date="June 25, 2024"
	imgSource="/the-waiting/a-silent-voice-banner.webp"
>
	<p class="poem-verse">
		A silent voice falls on inattentive ears:<br />

		Pleas for recognition of her persisting pain.<br />

		No one hears her cries,<br />

		Though she stands directly before them.<br />

		No one notices the crystal tears<br />

		Bleeding incessantly out her eyes,<br />

		As though a veil has chosen<br />

		To eternally enshroud her from existence.
	</p>

	<p class="poem-verse">
		A silent voice calls out to the wind,<br />

		Echoing a soundless symphony<br />

		Of sharply woven tears.<br />

		But only the reflections of her own misery<br />

		Echo back to counter her tune,<br />

		The winds failing to respond in kind.
	</p>

	<p class="poem-verse">
		A silent voice murmurs a muted prayer<br />

		And waits without expectation<br />

		For the noiseless abyss to take her in.<br />

		But even the insatiable vacuum<br />

		Ceases to open its maw in comforting hunger.
	</p>

	<p class="poem-verse">
		And so she withdraws herself into her cocoon<br />

		And gently closes her lips:<br />

		Perhaps she is inexorably fated<br />

		To suffer alone in ceaseless silence.
	</p>
</Poem>
