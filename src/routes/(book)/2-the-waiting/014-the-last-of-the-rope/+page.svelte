<Poem
	title="The Last of the Rope"
	date="June 11, 2024"
	imgSource="/the-waiting/the-last-of-the-rope-banner.webp"
>
	<p class="poem-verse">
		I will try with my everything,<br />

		If you choose to throw me<br />

		The last of the rope I’ve already wasted.
	</p>

	<p class="poem-verse">
		I will grab hold of it,<br />

		And I will climb,<br />

		With the last of the strength that you gave me in the past.
	</p>

	<p class="poem-verse">
		And although the line is already tight<br />

		From the weight of my failure pulling it down,<br />

		I will hold the rope close to my chest,<br />

		And I will climb,<br />

		And I will not let go,<br />

		Until maybe… one day<br />

		I can reach you at the top.
	</p>

	<p class="poem-verse">
		But I will not give up,<br />

		Until I’m there with you.<br />

		If you only throw it down<br />

		One final time.
	</p>
</Poem>
