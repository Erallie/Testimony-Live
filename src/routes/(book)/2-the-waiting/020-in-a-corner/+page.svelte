<Poem title="In a Corner" date="June 28, 2024" imgSource="/the-waiting/in-a-corner-banner.webp">
	<p class="poem-verse">
		The weeks roll by.<br />

		Everything stays the same.
	</p>

	<p class="poem-verse">
		Every night I bid hope goodbye,<br />

		Expecting him to be gone by morning.<br />

		And every morning I wake up,<br />

		And look in the corner and see him sitting there.
	</p>

	<p class="poem-verse">
		I thank him for sticking around.<br />

		He does not respond.<br />

		It’s okay; at least he’s there.<br />

		Or so I tell myself.
	</p>
</Poem>
