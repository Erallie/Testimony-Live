<Poem
	title="What Is a Friend?"
	date="October 27, 2020"
	imgSource="/the-waiting/what-is-a-friend-banner.webp"
>
	<p class="poem-verse">
		What is it like<br />

		To not have a soul?<br />

		To be devoid of not only sorrow,<br />

		But happiness?<br />

		Not only pain,<br />

		But love?
	</p>

	<p class="poem-verse">
		What is it like<br />

		To live without a friend?<br />

		To languish in your burdens alone,<br />

		To know that your thoughts are unseen,<br />

		That your cares are unheard?
	</p>

	<p class="poem-verse">
		<em>What is it like not to care?</em>
	</p>

	<p class="poem-verse">
		Would a friend heal your pain?<br />

		Would a friend know the answer?<br />

		Would a friend know <em>you?</em><br />

		Would a friend care?
	</p>

	<p class="poem-verse">
		But what is a <em>true</em> friend?<br />

		And where can one be found?
	</p>

	<p class="poem-verse">
		The answer to this, I may never know.<br />

		To live in the shadows<br />

		Without consolation,<br />

		Unseen by the world,<br />

		And to struggle alone,
	</p>

	<p class="poem-verse">
		That is the curse that I may ever bear:<br />

		To live without a friend,<br />

		And die without love.
	</p>
</Poem>
